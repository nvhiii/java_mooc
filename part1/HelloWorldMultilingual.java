// Write a program HelloWorldMultilingual.java that prints 
// "Hello World!" in as many human languages as you can.

public class HelloWorldMultilingual {

    public static void main(String[] args) {

        System.out.println("Shagatam Prithivi!"); // Bengali
        System.out.println("你好世界"); // Chinese
        System.out.println("Hello World!"); // English
        System.out.println("ہیلو دنیا والو"); // Urdu
        System.out.println("반갑다 세상아"); // Korean
        System.out.println("¡Hola mundo!"); // Spanish


    }

}